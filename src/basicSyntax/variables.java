package basicSyntax;

public class variables {
   public static void main(String[] args) {
	   int num1 = 6;
	   double num2 = 3.14;
	   String myFavorite = "私の好きな";
	   String endSentence = "です。";

	   System.out.println(num1);
	   System.out.println(num2);
	   System.out.println(myFavorite + "整数は" + num1 + endSentence);
	   System.out.println(myFavorite + "小数は" + num2 + endSentence);
   }
}